    private void redimencionarTabla(){
        TableColumnModel colum = tblOri.getColumnModel();
        colum.getColumn(0).setMinWidth(80);
        colum.getColumn(0).setMaxWidth(80);
        colum.getColumn(0).setWidth(80);
    }
    private void cargaTabla(){
        List<Origen> oris = OrigenControlador.recListOri();
        for(Origen ori : oris){
            Object[] obj = {ori.getOri_codigo(),ori.getOri_descri()};
            tabla.addRow(obj);
        }
    }
