    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        if(tblOri.getSelectedRow()>= 0){
            this.codigo = Integer.parseInt(tblOri.getValueAt( tblOri.getSelectedRow(),0)+"");
            this.desc = String.valueOf(tblOri.getValueAt(tblOri.getSelectedRow(),1));
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione un registro");
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    private void tblOriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOriMouseClicked
        if(evt.getClickCount()==2 && tblOri.getSelectedRow()>=0){
            this.codigo = Integer.parseInt(tblOri.getValueAt( tblOri.getSelectedRow(),0)+"");
            this.desc = String.valueOf(tblOri.getValueAt(tblOri.getSelectedRow(),1));
            this.dispose();
        }
    }//GEN-LAST:event_tblOriMouseClicked

    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        TableRowSorter sorter = new TableRowSorter(tabla);
        tblOri.setRowSorter(sorter);
        RowFilter filter = RowFilter.regexFilter("(?i)"+txtFiltro.getText().trim());
        sorter.setRowFilter(filter);
    }//GEN-LAST:event_txtFiltroKeyReleased

