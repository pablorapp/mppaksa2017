        private void setFilas(){
            String []Registros= new String[2];
            try {
                
                ResultSet rs = CotizacionControlador.consultaLista();
                while(rs.next()){
                        Registros[0]=rs.getString("tc_fec_cot");  
                        Registros[1]=rs.getString("tc_cot_mon");  
                        modelo.addRow(Registros);                    
                }
                
            } catch (Exception ex) {
                Logger.getLogger(ConsignatariosList.class.getName()).log(Level.SEVERE, null, ex);
            }           
            tbCotiza.setModel(modelo);  
            //tbCotiza.getColumnModel().getColumn(0).setPreferredWidth(50);
            //tbCotiza.getColumnModel().getColumn(1).setPreferredWidth(50);           
        }            

