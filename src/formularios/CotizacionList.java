    private void btnElimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElimActionPerformed
        String cod = tbCotiza.getValueAt(tbCotiza.getSelectedRow(),0).toString();
        int reply = JOptionPane.showConfirmDialog(null, "Desea eliminar la CotizaciÃ³n de la fecha : "+cod +" ?", modo, JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION){
          String res = CotizacionControlador.eliCoti(cod);
                if(!res.equals("")){
                    JOptionPane.showMessageDialog(null, res);
                }else{
                    JOptionPane.showMessageDialog(null, "Eliminado Exitosamente");
                    cleanJtable();
                    setFilas();
                }          
        }    
    }//GEN-LAST:event_btnElimActionPerformed

    
    private void cleanJtable(){
                for (int i = 0; i < tbCotiza.getRowCount(); i++) {
                modelo.removeRow(i);
                i-=1;
                }
    }    


