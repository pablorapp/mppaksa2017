    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        TableRowSorter sorter = new TableRowSorter(tabla);
        tblTarifa.setRowSorter(sorter);
        RowFilter filter = RowFilter.regexFilter("(?i)"+txtFiltro.getText().trim());
        sorter.setRowFilter(filter);
    }//GEN-LAST:event_txtFiltroKeyReleased

    private void tblTarifaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTarifaMouseClicked
        if(evt.getClickCount()==2 && tblTarifa.getSelectedRow()>=0){
            this.codigo = Integer.parseInt(tblTarifa.getValueAt( tblTarifa.getSelectedRow(),0)+"");
            this.cab = Integer.parseInt(tblTarifa.getValueAt( tblTarifa.getSelectedRow(),1)+"");
            this.desc = String.valueOf(tblTarifa.getValueAt(tblTarifa.getSelectedRow(),2));
            this.dispose();
        }
    }//GEN-LAST:event_tblTarifaMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(tblTarifa.getSelectedRow()>= 0){
            this.codigo = Integer.parseInt(tblTarifa.getValueAt( tblTarifa.getSelectedRow(),0)+"");
            this.cab = Integer.parseInt(tblTarifa.getValueAt( tblTarifa.getSelectedRow(),1)+"");
            this.desc = String.valueOf(tblTarifa.getValueAt(tblTarifa.getSelectedRow(),2));
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione un registro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

