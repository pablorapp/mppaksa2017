    private void redimencionarTabla(){
        TableColumnModel colum = tblTarifa.getColumnModel();
        colum.getColumn(0).setMinWidth(80);
        colum.getColumn(0).setMaxWidth(80);
        colum.getColumn(0).setWidth(80);
        colum.getColumn(1).setMinWidth(80);
        colum.getColumn(1).setMaxWidth(80);
        colum.getColumn(1).setWidth(80);
    }
    private void cargaTabla(){
        try {
            ResultSet rs = TarifasControlador.consultaLista();
            while(rs.next()){
                Object[] obj = {rs.getString("tt_cod_cab"),rs.getInt("tt_codigo"),rs.getString("tt_abrevia")};
                tabla.addRow(obj);
            }
        } catch (SQLException ex) {
            Logger.getLogger(SelecConsignatario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
